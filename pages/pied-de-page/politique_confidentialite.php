En quoi consistent les Données Personnelles ?
Au sein de la présente Politique de protection des données, les « Données Personnelles » désignent toute information permettant d’identifier un Utilisateur directement ou indirectement notamment, par exemple, en référence à son nom, son adresse email, son adresse IP (ci-après les « Données Personnelles »).<br><br>

Sont exclues des Données Personnelles, les informations anonymisées, c’est-à-dire les données personnelles qui ont fait l’objet d’un traitement approprié pour les rendre non identifiables afin d’empêcher leur identification de manière irréversible.<br><br>

